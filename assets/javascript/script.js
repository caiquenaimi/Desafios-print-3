function passar() {
    var destaque1 = window.document.querySelector('section#principal1')
    destaque1.style.backgroundColor = '#5c5757'
    destaque1.style.outline = 'double whitesmoke'
    destaque1.style.transition = '1s'
    destaque1.style.scale = '1.1'
} /* essa seção seleciona o section#principal1 e ao colocar o mouse em cima da uma pequena modificada */

function sair() {
    var destaque1 = window.document.querySelector('section#principal1')
    destaque1.style.backgroundColor = '#141212'
    destaque1.style.outline = 'none'
    destaque1.style.transition = '1s'
    destaque1.style.scale = 'none'
} /* essa seção seleciona o section#principal1 e ao sair com o mouse o retorna ao normal */

